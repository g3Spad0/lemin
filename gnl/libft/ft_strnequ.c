#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, int n)
{
	if (!n)
		return (1);
	if (!s1 || !s2 || n < 0)
		return (0);
	while (*s1 && *s2 && (*s1 == *s2))
	{
		s1++;
		s2++;
		n--;
		if (!n)
			return (1);
	}
	if (!*s1 && !*s2)
		return (1);
	return (0);
}
